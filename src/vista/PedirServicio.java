    private void cmbtiposervaddItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbtiposervaddItemStateChanged
        if (cmbtiposervadd.getSelectedItem().equals("Pasajeros")) {
            txtvalorserv.setText("100000");
        }
        if (cmbtiposervadd.getSelectedItem().equals("Alimentos")) {
            txtvalorserv.setText("150000");
        }
    }//GEN-LAST:event_cmbtiposervaddItemStateChanged

    private void cmbcatItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbcatItemStateChanged
        if (cmbcat.getSelectedItem().equals("Normal")) {
            txtvalorcat.setText("1000");
        }
        if (cmbcat.getSelectedItem().equals("Especial")) {
            txtvalorcat.setText("5000");
        }
        if (cmbcat.getSelectedItem().equals("Urgente")) {
            txtvalorcat.setText("15000");
        }
    }//GEN-LAST:event_cmbcatItemStateChanged

